/*
 * Performs a DFS search from the first 0 in the given string of binary.
 * If the result is not a multiple of P, the fixed polynomial placement is
 * invalid. solution is modified from a solution generated by AI using the
 * prompt "If I had a string that was representing a NxN grid (e.g. 010110000
 * for N = 3), how could I write a function that uses depth first search to find
 * the number of reachable 0s starting from the first 0 in the string?"
 *
 * @string: bit-mask of polyomino placement in the form of an array of
 * characters.
 *
 * @x: start grid column for dfs.
 *
 * @y: start grid row for dfs.
 *
 * @return: how many empty cells were found from the starting position.
 */

int visited[P][P];
int dx[] = {-1, 1, 0, 0};
int dy[] = {0, 0, -1, 1};

int dfs(char *string, int x, int y) {

  if (x < 0 || x >= P || y < 0 || y >= P) {
    return 0;
  }

  if (visited[x][y]) {
    return 0;
  }
  if (string[x * P + y] != '0') {
    return 0;
  }
  visited[x][y] = 1;

  int count = 1;

  for (int dir = 0; dir < 4; dir++) {
    int nx = x + dx[dir];
    int ny = y + dy[dir];
    count += dfs(string, nx, ny);
  }

  return count;
}

/*
 * Uses the dfs function above to check whether
 * or not a given polyomino placement on the grid is valid.
 *
 * @string: bit-mask of polyomino placement in the form of an array of
 * characters.
 *
 * @return: -1 if grid is full, otherwise the number of
 *   empty cells that were found from the first empty cell found
 */
int poly_valid(char *string) {
  int start = -1;
  for (int i = 0; i < Pmax; i++) {
    if (string[i] == '0') {
      start = i;
      break;
    }
  }

  if (start == -1) {
    return 0;
  }

  memset(visited, 0, sizeof(visited));

  int start_x = start / P;
  int start_y = start % P;

  int total = dfs(string, start_x, start_y);

  return total;
}

