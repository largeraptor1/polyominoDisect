/*
 * Bucket sorts the initial linked list of polyominoes into separate arrays
 * based on their smallest cell index.
 */
void build_heads() {
  for (int i = 0; i < Pmax; i++)
    heads[i] = NULL;

  Poly *current = tHead;
  int counter = 0;
  while (current != NULL) {
    Poly *next = current->next;

    int idx = current->cells[0]; // bucket by first cell
    if (idx < 0 || idx >= Pmax) {
      printf("Warning: invalid cell index %d\n", idx);
      current = next;
      continue;
    }

    current->next = NULL; // detach node from original list
    if (heads[idx] == NULL) {
      heads[idx] = current;
    } else {
      insert_front(&heads[idx], current);
    }

    counter++;
    current = next;
  }
  printf("Total polys bucketed: %d\n", counter);
}

