/* tsiz is the size of the current tile at entry, and is thus the index for the
point(s) to be added.  x/y are the point at the head of the untried stack. */

void recurse(int tsiz, int x, int y) {
  int ix, iy, tx, ty, cx, cy;
  int tl, tr, tu, td;

  /* Repeat until the untried set is exhausted ... */

  ix = x;
  iy = y;
  while (ix != Eos && iy != Eos) {

    /* 1. Remove an (arbitrary) element from the untried set ... */

    cx = succpt[ix + Xoff][iy + Yoff][0];
    cy = succpt[ix + Xoff][iy + Yoff][1];

    /* 2. Place a cell at this point ... */

    tile[tsiz][0] = ix;
    tile[tsiz][1] = iy;

    /* 3. Count this new polynomio, process if size P ... */

    t[tsiz + 1]++;

    if (tsiz == P - 1) {
      /*printf("\n");*/
      logTile(tsiz + 1);
    }

    /* 4. If the size is less than P ... */

    if (tsiz < P - 1) {

      /* (a) Add *new* neighbours to the untried set ... */

      if (freept[ix + Xoff][iy + Yoff + 1]) {
        succpt[ix + Xoff][iy + Yoff + 1][0] = cx;
        succpt[ix + Xoff][iy + Yoff + 1][1] = cy;
        cx = ix;
        cy = iy + 1;

        freept[cx + Xoff][cy + Yoff] = False;
        tu = True;
      } else {
        tu = False;
      }
      if (freept[ix + Xoff + 1][iy + Yoff]) {
        succpt[ix + Xoff + 1][iy + Yoff][0] = cx;
        succpt[ix + Xoff + 1][iy + Yoff][1] = cy;
        cx = ix + 1;
        cy = iy;

        freept[cx + Xoff][cy + Yoff] = False;
        tr = True;
      } else {
        tr = False;
      }
      if (freept[ix + Xoff - 1][iy + Yoff]) {
        succpt[ix + Xoff - 1][iy + Yoff][0] = cx;
        succpt[ix + Xoff - 1][iy + Yoff][1] = cy;
        cx = ix - 1;
        cy = iy;

        freept[cx + Xoff][cy + Yoff] = False;
        tl = True;
      } else {
        tl = False;
      }
      if (freept[ix + Xoff][iy + Yoff - 1]) {
        succpt[ix + Xoff][iy + Yoff - 1][0] = cx;
        succpt[ix + Xoff][iy + Yoff - 1][1] = cy;
        cx = ix;
        cy = iy - 1;

        freept[cx + Xoff][cy + Yoff] = False;
        td = True;
      } else {
        td = False;
      }

      /* (b) Call this algorithm recursively ... */

      recurse(tsiz + 1, cx, cy);

      /* (c) Remove the new neighbours from the untried set ... */

      if (td) {
        freept[cx + Xoff][cy + Yoff] = True;

        tx = succpt[cx + Xoff][cy + Yoff][0];
        ty = succpt[cx + Xoff][cy + Yoff][1];
        cx = tx;
        cy = ty;
      }
      if (tl) {
        freept[cx + Xoff][cy + Yoff] = True;

        tx = succpt[cx + Xoff][cy + Yoff][0];
        ty = succpt[cx + Xoff][cy + Yoff][1];
        cx = tx;
        cy = ty;
      }
      if (tr) {
        freept[cx + Xoff][cy + Yoff] = True;

        tx = succpt[cx + Xoff][cy + Yoff][0];
        ty = succpt[cx + Xoff][cy + Yoff][1];
        cx = tx;
        cy = ty;
      }
      if (tu) {
        freept[cx + Xoff][cy + Yoff] = True;

        tx = succpt[cx + Xoff][cy + Yoff][0];
        ty = succpt[cx + Xoff][cy + Yoff][1];
        cx = tx;
        cy = ty;
      }
    }

    /* 5. Remove newest cell ... */

    /* n/a */

    tx = succpt[ix + Xoff][iy + Yoff][0];
    ty = succpt[ix + Xoff][iy + Yoff][1];
    ix = tx;
    iy = ty;
  }
}

/*
 * Converts a given polyomino placement into a string of 0s and 1s for a
 * binary representation of the given polyomino.
 *
 * @cells: array of the indexes of cells that are occupied by a given polyomino.
 *
 * @return pointer to an array of chars that is the binary representation of the
 * polyomino placement.
 */
char *int_to_binary(int cells[P]) {
  char *binary = malloc(Pmax * sizeof(char));
  if (!binary) {
    return NULL;
  }

  for (int i = 0; i < Pmax; i++) {
    binary[i] = '0';
  }

  for (int i = 0; i < P; i++) {
    binary[cells[i]] = '1';
  }

  return binary;
}

/*
 * Swap sorts a polyomino's array of cell indexes into ascending order.
 *
 * @cells: array of the indexes of cells that are occupied by a given polyomino.
 */
void sort_cells(int cells[P]) {
  for (int i = 0; i < P - 1; i++) {
    for (int j = i + 1; j < P; j++) {
      if (cells[j] < cells[i]) {
        int temp = cells[i];
        cells[i] = cells[j];
        cells[j] = temp;
      }
    }
  }
}

/*
 * Iterates through the generated linked list of polyomino placements
 * and removes the ones that are invalid placements from the list.
 *
 * @head: a linked list of Poly structs.
 *
 * @return: head but with any invalid polyomino placements removed.
 */
Poly *pruning(struct Poly *head) {
  struct Poly *current = head;
  struct Poly *prev = NULL;
  int count = 0;
  while (current != NULL) {
    current->mask = make_mask(current->cells);
    sort_cells(current->cells);
    char *binaryRep = int_to_binary(current->cells);
    if (poly_valid(binaryRep) % P != 0) {
      if (prev != NULL) {
        prev->next = current->next;
      } else {
        prev = current->next;
      }
      Poly *now = current;
      current = current->next;
      free(now);
    } else {
      prev = current;
      current = current->next;
      count++;
    }
    free(binaryRep);
  }
  totalPolys = count;
  return head;
}

/*
 * Similar to the pruning function above, but only checks a single polyomino
 * place from its mask.
 *
 * @mask: 64 bit-mask of the given partial grid cover.
 *
 * @return: 0 if partial cover is invalid, 1 otherwise.
 */
int prune(uint64_t mask) {
  char grid[Pmax];
  for (int i = 0; i < Pmax; i++) {
    if ((mask & (1ULL << i)) != 0) {
      grid[i] = '1';
    } else {
      grid[i] = '0';
    }
  }
  int pruned = poly_valid(grid);
  if (pruned % P != 0) {
    return 0;
  } else {
    return 1;
  }
}

