/*converts binary of fixed polyomino into a string for DPS for validity checking.
Current solution built using using AI with prompt "In c code, how could I convert 
an integer into a string of its binary?" converted its solution to work in this code.*/

char* int_to_binary(uint64_t n) {
  int size = Pmax;
  char *string = malloc(Pmax + 1);

  if (string == NULL) {
    return NULL;
  }

  for (int i = size - 1; i >= 0; i--) {
    string[size - 1 - i] = (n & (1 << i)) ? '1' : '0';
  }

  string[size] = '\0';

  return string;
}

/*Performs a DFS search from the first 0 in the given string of binary. 
If the result is not a multiple of P, the fixed polynomial placement is invalid.
solution is modified from a solution generated by AI using the prompt "If I had a 
string that was representing a NxN grid (e.g. 010110000 for N = 3), how could I write 
a function that uses depth first search to find the number of reachable 0s starting 
from the first 0 in the string?"*/

int visited[Pmax][Pmax];
int dx[] = {-1, 1, 0, 0};
int dy[] = {0, 0, -1, 1};

int dfs (char* string, int x, int y) {

  if (x < 0 || x >= P || y < 0 || y>= P) {
    return 0;
  }

  if (visited[x][y]) {
    return 0;
  }
  if(string[x * P + y] != '0') {
    return 0;
  }
  visited[x][y] = 1;

  int count = 1;

  for (int dir = 0; dir < 4; dir++) {
    int nx = x + dx[dir];
    int ny = y + dy[dir];
    count += dfs(string, nx, ny);
  }

  return count;
}

int poly_valid(char *string) {
  int start = -1;
  for (int i = 0; i < Pmax; i++) {
    if (string[i] == '0') {
      start = i;
      break;
    }
  }

  if (start == -1) {
    return 0;
  }

  memset(visited, 0, sizeof(visited));

  int start_x = start / P;
  int start_y = start % P;

  int total = dfs(string, start_x, start_y);

  return total;

}


