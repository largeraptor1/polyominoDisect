/* Takes the linked list of (all/valid) polyomino positions, builds the exact
cover DLX problem, then generates all distinct covers.  Derived from the 
solveLatin*() routines in the MyWork/DLX2/scs*.c routines.  Our exact cover 
array has P^2 rows, one for each cell; ie, positions 0..P^2-1, in term or rows &
cols 0..P-1.  There are covp (currently = copp) cols, one for each of the Poly 
positions in the list, numbered 0..covp-1.  Each Poly col has P entries. */

void genCov(void)
  {
  int r,c, i,j, pCol;
  Poly *ptr;
  exact_t *e = exact_alloc();

  for (r = 0; r < P; r++)
    { for (c = 0; c < P; c++) { exact_declare_row(e, r*P + c, 1); } }

  for (ptr = tHead, pCol = 0; ptr != NULL; ptr = ptr->next, pCol++)
    {
    exact_declare_col(e, pCol, 1);
    for (i = 0; i < P; i++)
      { exact_declare_entry(e, ptr->cells[i], pCol); }
    }
  
  int stk;  
  const int *b;

  /* This is ... a bit of a kludge.  We need to have some sort of col to covered
  rows lookup table.  ?Build this when we're setting up the problem. */

  while ((b = exact_solve(e, &stk)) != NULL) 
    { 
    codc++;

    printf("codc=%d, b[0..%d] =", codc, stk);
    for (i = 0; i < stk; i++)
      { 
      printf(" %d", b[i]);
      for (j = 0; j < P*P; j++)
         {
         if (exact_is_entry(e, j, b[i]) == 1) 
           { tiling[j/P][j%P] = i; }
         }
      }
    printf("\n");
    for (r = 0; r < P; r++)
      {
      for (c = 0; c < P; c++)
        /*{ printf(" %d", tiling[r][c]); }*/
        { printf(" %c", 'A' + tiling[r][c]); }
      printf("\n");
      }
    }

  exact_free(e);
  }

